/**
 * Get evidence documents for a payout request
 */
export function getEvidenceForPayoutRequest(payoutRequestId: string): EvidenceDocument[] {
  return mockEvidenceDocuments.filter((e) => e.payoutRequestId === payoutRequestId);
}

/**
 * Get approval history for a payout request
 */
export function getApprovalHistoryForPayoutRequest(payoutRequestId: string): PayoutApprovalEvent[] {
  return mockPayoutApprovalEvents
    .filter((e) => e.payoutRequestId === payoutRequestId)
    .sort((a, b) => new Date(a.createdAt).getTime() - new Date(b.createdAt).getTime());
}

/**
 * Get pending approval requests
 */
export function getPendingApprovalRequests(): PayoutRequest[] {
  return mockPayoutRequests.filter((r) => r.status === "PENDING_APPROVAL");
}
